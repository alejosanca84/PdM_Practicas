/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	// Arreglo constante que define la secuencia de tiempos de ENCENDIDO
	const uint32_t TIEMPOS[] = {500, 100, 100, 1000};

	// Variable de estado para saber en qué paso de la secuencia estamos
	uint8_t sequence_index = 0;

	// Variable de estado para saber si el LED está encendido o apagado
	// Se inicia en false, ya que el LED empieza apagado.
	bool_t led_is_on = false;

	// Nuestra única variable de retardo
	delay_t led_delay;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  /* USER CODE BEGIN 2 */

  // Calculamos el número total de pasos en nuestra secuencia
  const uint8_t sequence_size = sizeof(TIEMPOS) / sizeof(TIEMPOS[0]);

  // Inicializamos el retardo con la duración del PRIMER paso de la secuencia
  delayInit(&led_delay, TIEMPOS[sequence_index]);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    if ( delayRead(&led_delay) )
    {
        // Cada vez que el retardo se cumple, invertimos el estado del LED
        HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);

        // Actualizamos nuestra variable de estado del LED
        led_is_on = !led_is_on;

        // --- AQUÍ ESTÁ LA LÓGICA CLAVE DE LA MÁQUINA DE ESTADOS ---

        // Si acabamos de APAGAR el LED, significa que un ciclo completo (ON-OFF) ha terminado.
        // Es el momento de prepararse para el SIGUIENTE ciclo con un nuevo tiempo.
        if (!led_is_on)
        {
            // Avanzamos al siguiente tiempo en la secuencia
            sequence_index++;

            // Si llegamos al final de la secuencia, volvemos al inicio
            if (sequence_index >= sequence_size)
            {
                sequence_index = 0;
            }

            // --- CAMBIO AQUÍ ---
                        // Verificamos que el retardo NO esté corriendo ANTES de cambiar su duración.
                        // Esta es una medida de seguridad para una API robusta.
                        if (!delayIsRunning(&led_delay))
                        {
                            delayWrite(&led_delay, TIEMPOS[sequence_index]);
                        }
        }
        // Si acabamos de ENCENDER el LED, no hacemos nada.
        // Dejamos que el retardo se complete de nuevo con la MISMA duración
        // para cumplir con el ciclo de trabajo del 50%.
    }
  }
  /* USER CODE END 3 */
}

